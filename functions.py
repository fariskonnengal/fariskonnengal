#Area of circle
radius=float(input("enter the radius"))

def calculate_area_circle(radius):
    area =3.14*radius**2
    return area
print(calculate_area_circle(radius))


#area of rectangle
length=float(input("enter the lenth"))
width=float(input("enter the width"))
def calculate_area_length(lenth,width):
    area = lenth*width
    return area
print(calculate_area_length(length,width))

